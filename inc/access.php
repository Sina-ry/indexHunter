##    FileName : /lib/access.php
##    설명 : 관리자 전용 페이지의 접근 권한을 체크한다.
######################################################################################################




if( defined('_AUTH_') ) {

//-----------------------------------------------------------------------------------
switch( _AUTH_ ) {
//-----------------------------------------------------------------------------------
case 'ADMIN' :
//-----------------------------------------------------------------------------------

	if ($_SESSION['_admin']  <> "ROOT" ) {
			echo "<script>alert('섹션이 종료되었습니다 다시 로긴해 주세요 '); location.replace('/index.php'); </script>\n";
			exit;		
	}

//-----------------------------------------------------------------------------------
break;
//-----------------------------------------------------------------------------------
case 'MEMBER' :
//-----------------------------------------------------------------------------------

	if ($_SESSION['_sign'] <> "USER" && $_SESSION['_sign'] <> "ADMIN") {
			echo "<script>alert('로그인을 해 주시기 바랍니다 .  '); </script>\n";
			echo "<script>location.replace('/index.php'); </script>\n";
			exit;
	}

//-----------------------------------------------------------------------------------
break;
//-----------------------------------------------------------------------------------


} // end switch;

}

?>